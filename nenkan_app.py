def _trim_indices(n, limit):
    """全 n 個の小単元から、流れを保って limit 個のインデックスを選ぶ。
    導入(先頭)と本番・まとめ(末尾2つ)は必ず残し、中間(準備段階)を均等に間引く。"""
    idxs = list(range(n))
    if not limit or n <= limit:
        return idxs
    if limit <= 2:
        keep = ([0] if n >= 1 else []) + ([n - 1] if n >= 2 else [])
        return keep[:limit] if limit < 2 else keep
    head = [0]                                   # 導入
    tail = [n - 2, n - 1]                         # 本番・まとめ
    mid_quota = limit - len(head) - len(tail)
    mid = list(range(1, n - 2))
    if mid_quota > 0 and mid:
        step = len(mid) / mid_quota
        picked = [mid[int(i * step)] for i in range(mid_quota)]
        return head + picked + tail
    return head + tail


