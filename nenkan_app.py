def _flat_rows(data, bu, subj, div_idx, units, duration, show_eval, show_act=True, show_ref=True, show_mat=True):
    rows = []
    for i, u in enumerate(units):
        r = render_edited(data, bu, subj, div_idx, u); month = month_label(i, duration)
        gt = goal_text(r["目標"])
        for j, b in enumerate(r["blocks"]):
            act, ref = split_activities(b["活動"])
            row = {"月": month if j == 0 else "", "単元名": r["単元名"] if j == 0 else "",
                   "段階": r["段階表示"], "領域": b["領域"], "目標": gt if j == 0 else "",
                   "学習内容": "\n".join(content_lines(b))}
