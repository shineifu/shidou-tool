def export_kobetsu_word(subj, goals3, path):
    """個別の指導計画をWord出力（3観点・表形式・児童名なし）。"""
    from docx import Document
    from docx.shared import Pt, RGBColor
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement
    doc = Document()
    h = doc.add_paragraph()
    r = h.add_run("個別の指導計画"); r.bold = True
    r.font.size = Pt(18); r.font.color.rgb = RGBColor(0x3A, 0x6B, 0x5C)
    doc.add_paragraph().add_run(f"教科：{subj}").font.size = Pt(11)
    table = doc.add_table(rows=1, cols=3); table.style = "Table Grid"
    hdr = table.rows[0].cells
    for c, name in enumerate(["観点", "指導目標", "段階"]):
        hdr[c].text = name
        for pp in hdr[c].paragraphs:
            for rn in pp.runs:
                rn.font.bold = True; rn.font.size = Pt(10)
        # ヘッダ背景色
        tcPr = hdr[c]._tc.get_or_add_tcPr()
        shd = OxmlElement('w:shd'); shd.set(qn('w:val'), 'clear')
        shd.set(qn('w:fill'), '3A6B5C'); tcPr.append(shd)
        for pp in hdr[c].paragraphs:
            for rn in pp.runs:
                rn.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
    for pillar in PILLAR_ORDER:
        items = goals3.get(pillar, [])
        if not items:
            cells = table.add_row().cells
            cells[0].text = PILLAR_LABELS[pillar]; cells[1].text = "―"; cells[2].text = ""
            continue
        first_row_idx = len(table.rows)
        for k, it in enumerate(items):
            cells = table.add_row().cells
            cells[0].text = PILLAR_LABELS[pillar] if k == 0 else ""
            cells[1].text = it["text"]
            cells[2].text = f"{it['bu']}{it['stage']}段階" if it.get("bu") else ""
            for c in range(3):
                for pp in cells[c].paragraphs:
                    for rn in pp.runs:
                        rn.font.size = Pt(10)
        # 観点セルを縦結合
        if len(items) > 1:
            a = table.cell(first_row_idx, 0)
            b = table.cell(first_row_idx + len(items) - 1, 0)
            a.merge(b)
    # 列幅
    from docx.shared import Mm
    for r_ in table.rows:
        r_.cells[0].width = Mm(40); r_.cells[1].width = Mm(170); r_.cells[2].width = Mm(35)
    doc.save(path)


def export_kobetsu_excel(subj, goals3, path):
    """個別の指導計画をExcel出力（3観点・児童名なし）。"""
    from openpyxl import Workbook
    from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    wb = Workbook(); ws = wb.active; ws.title = "個別の指導計画"
    thin = Side(style="thin", color="999999")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    ws["A1"] = "個別の指導計画"; ws["A1"].font = Font(size=16, bold=True, color="3A6B5C")
    ws["A2"] = f"教科：{subj}"; ws["A2"].font = Font(size=11)
    ws["A4"] = "観点"; ws["B4"] = "指導目標"; ws["C4"] = "段階"
    for c in ("A4", "B4", "C4"):
        ws[c].font = Font(bold=True, color="FFFFFF")
        ws[c].fill = PatternFill("solid", fgColor="3A6B5C")
        ws[c].border = border; ws[c].alignment = Alignment(vertical="center")
    row = 5
    for pillar in PILLAR_ORDER:
        items = goals3.get(pillar, [])
        n = max(1, len(items))
        ws.cell(row=row, column=1, value=PILLAR_LABELS[pillar]).font = Font(bold=True)
        if items:
            for k, it in enumerate(items):
                ws.cell(row=row + k, column=2, value=it["text"])
                tag = f"{it['bu']}{it['stage']}段階" if it.get("bu") else ""
                ws.cell(row=row + k, column=3, value=tag)
        else:
            ws.cell(row=row, column=2, value="―")
        for rr in range(row, row + n):
            for cc in range(1, 4):
                cell = ws.cell(row=rr, column=cc)
                cell.border = border
                cell.alignment = Alignment(wrap_text=True, vertical="top")
        if len(items) > 1:
            ws.merge_cells(start_row=row, start_column=1, end_row=row + len(items) - 1, end_column=1)
        row += n
    ws.column_dimensions["A"].width = 22
    ws.column_dimensions["B"].width = 70
    ws.column_dimensions["C"].width = 14
    wb.save(path)


