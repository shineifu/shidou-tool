def make_plan(data, bu, subj, div_idx, base_stage, duration):
    div = get_division(data, bu, subj, div_idx)
    pairs = all_ryoiki(div, base_stage)           # [(grp, ryoiki), ...]
    ryoiki_list = [r for (_, r) in pairs]
