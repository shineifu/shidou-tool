def split_even(items, n):
    chunks = [[] for _ in range(max(1, n))]
    for i, it in enumerate(items):
        chunks[i % len(chunks)].append(it)
    return chunks


def split_groups(lst, n):
    R = len(lst); base, extra = divmod(R, n)
    out, idx = [], 0
    for i in range(n):
        size = base + (1 if i < extra else 0)
        out.append(lst[idx:idx + size]); idx += size
    return [g for g in out if g]


def allocate(ryoiki_list, n):
    R = len(ryoiki_list)
    if R == 0:
        return []
    if n <= R:
        return [[(r, 0, 1) for r in g] for g in split_groups(ryoiki_list, n)]
    seq = [ryoiki_list[i % R] for i in range(n)]
    occ_total = {r: seq.count(r) for r in ryoiki_list}
    cnt, out = {}, []
    for r in seq:
        o = cnt.get(r, 0); cnt[r] = o + 1
        out.append([(r, o, occ_total[r])])
    return out


