def seikatsu_arrange(bu, n_units):
    """学部に合うテーマを、月の目安に沿って年間 n_units 個配置。(themes, dur)
    同じ月に複数候補があるときは、小単元が用意されたテーマを優先する。"""
    avail = [t for t in SEIKATSU_THEMES if bu in t["bu"]]
    dur = len(SCHOOL_MONTHS) // n_units
    used, result = set(), []

    def has_subunits(t):
        return 1 if t["name"] in SEIKATSU_SUBUNITS else 0

    for i in range(n_units):
        rep = SCHOOL_MONTHS[i * dur:(i + 1) * dur]
        # その月に合う候補を集め、小単元の有無で優先
        cands = [t for t in avail if t["name"] not in used and any(mn in rep for mn in t["months"])]
        cands.sort(key=has_subunits, reverse=True)
        best = cands[0] if cands else None
        if best is None:
            # 季節指定なし(通年)や残りから、小単元ありを優先
            rest = [t for t in avail if t["name"] not in used]
            rest.sort(key=has_subunits, reverse=True)
            best = rest[0] if rest else None
        if best is None:
            best = avail[len(result) % len(avail)]
        used.add(best["name"])
        result.append(best)
    return result, dur


