def get_subunits(theme_name, bu, stage="1", limit=None, indices=None):
    """テーマの小単元を、学部別表記＋段階別学習内容で返す。
    indices を与えるとそのインデックスのみ（順序保持）。
    無ければ limit で先頭から流れを保って間引く。"""
    k = _bu_key(bu)
    sk = k + str(stage)            # 例 "小2","中1","高2"
    full = SEIKATSU_SUBUNITS.get(theme_name, [])

    def pick_bu(d):
        return d.get(k, next(iter(d.values())))

    def pick_stage(d):
        if sk in d:
            return d[sk]
        for kk in d:
            if kk.startswith(k):
                return d[kk]
        return next(iter(d.values()))

    def render(su):
        return {
            "title": pick_bu(su["title"]),
            "activity": pick_bu(su["activity"]),
            "subjects": {subj: [{"領域": it["領域"], "学習": pick_stage(it["学習"])} for it in items]
                         for subj, items in su["subjects"].items()},
        }

    if indices is not None:
        return [render(full[i]) for i in indices if 0 <= i < len(full)]
    sel = _trim_indices(len(full), limit) if limit else list(range(len(full)))
    return [render(full[i]) for i in sel]


