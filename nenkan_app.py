def content_lines(b):
    """学習内容を読みやすく整形して行リストで返す。
    - 行頭の領域名の繰り返しは付けない（領域は別途見出しに出る）
    - 「ア　…身に付けることができるよう指導する。」等の総則文は省く
    - 「（ｱ）次のような知識及び技能を…」→「【知識・技能】」の小見出し
    - 「（ｲ）次のような思考力…」→「【思考・判断・表現】」の小見出し
    - 「└ア …」等の下位事項は『・』で示す
    - prefix（柱・領域）は先頭1行だけに付ける
    """
    raw = b.get("学習内容", [])
    if not b.get("prefix"):
        return list(raw)
    out = []
    for x in raw:
        s = x.replace("\u3000", " ").strip()
        # 総則的な導入文は省略
        if _re_cl.match(r'^[ア-ン]\s', s) and ("身に付けることができるよう指導" in s or "ものとする" in s):
            continue
        # 知識技能／思考力 の見出し化
        if _re_cl.match(r'^（[ｱ-ﾝ]）', s):
            if "知識及び技能" in s:
                out.append("【知識・技能】"); continue
            if "思考力" in s:
                out.append("【思考・判断・表現】"); continue
            if "学びに向かう" in s:
                out.append("【学びに向かう力】"); continue
            # その他の（ｱ）項目は本文として残す
            body = _re_cl.sub(r'^（[ｱ-ﾝ]）\s*', '', s)
            out.append("・" + body); continue
        # 下位事項 └ア → ・
        m = _re_cl.match(r'^└?\s*([ア-ンｱ-ﾝ])\s*(.+)', s)
        if m:
            out.append("・" + m.group(2).strip()); continue
        out.append("・" + s)
    if not out:
        out = ["・" + x.replace("\u3000", " ").strip() for x in raw]
    # 先頭行にだけ 柱略＋領域 を付す（重複防止）
    head = (f"{b['柱略']}　{b['領域']}" if b.get("柱略") else b["領域"])
    return [f"〔{head}〕"] + out


def split_activities(items):
    """活動アイテムを (学習活動例=AI補足/手入力, 関連事項=解説) に振り分ける。"""
    act = [it for it in items if it.get("s") != "解説"]
    ref = [it for it in items if it.get("s") == "解説"]
    return act, ref


def mat_lines(items, mark):
    """教材例（出典表記なし）。"""
    if not items:
        return "―"
    return "\n".join(f"{mark}{it['t']}" for it in items)


def act_lines(items, mark):
    """学習活動例（ソフトオリジナルのみ・出典表記なし）。"""
    if not items:
        return "―"
    return "\n".join(f"{mark}{it['t']}" for it in items)


def ref_lines(items, mark):
    """学習指導要領の関連事項（出典ページつき）。読みにくい断片は除外・整形。"""
    import re as _re
    if not items:
        return "―"
    cleaned = []
    for it in items:
        t = it.get("t", "").strip()
        # 不自然な断片を除外：見出し記号で始まるもの、原文の構造語が混ざるもの
        if _re.match(r'^[（(]?[ｱ-ﾝア-ン○⑤-⑨]', t):
            continue
        if "指導に当たって" in t or "第４章" in t or "児童生" in t:
            continue
        # 途中で切れている可能性が高い（句点で終わらない長文）はそのまま出すが、
        # マーカーのみ・極端に短いものは捨てる
        if len(t) < 8:
            continue
        cleaned.append(it)
    if not cleaned:
        return "―"

    def _lab(it):
        p = f" p{it['p']}" if it.get('p') else ""
        return f"{mark}{it['t']}　〔学習指導要領解説{p}〕"
    return "\n".join(_lab(it) for it in cleaned)


