    def redraw():
        for w in inner.winfo_children():
            w.destroy()
        if not state["units"]:
            tk.Label(inner, text="学部・教科・段階・期間を選んで「計画を生成」を押してください。",
                     font=JP, fg="#888").pack(pady=40)
            return
        for i, u in enumerate(state["units"]):
            r = render_edited(data, state["bu"], state["subj"], state["div"], u)
            month = month_label(i, state["duration"])
            wrap = tk.Frame(inner, pady=6); wrap.pack(fill="x")
            top = tk.Frame(wrap); top.pack(fill="x")
            btns = tk.Frame(top); btns.pack(side="right")
            tk.Button(btns, text="↑ 前の単元と入替", command=lambda i=i: move(i, -1)).pack(side="left")
            tk.Button(btns, text="↓ 次の単元と入替", command=lambda i=i: move(i, 1)).pack(side="left", padx=(3, 8))
            tk.Button(btns, text="編集", command=lambda i=i: open_edit(i)).pack(side="left")
