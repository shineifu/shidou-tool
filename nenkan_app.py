    def swap_theme(i):
        """大単元の別の案：季節適合（通年含む）＋小単元ありを優先して並べ、押すたびに巡回。"""
        mlabel, cur_theme, _ = st["plan"][i]
        used = {t["name"] for j, (_, t, _) in enumerate(st["plan"]) if j != i}
        rep = st.get("slot_months", [])[i] if i < len(st.get("slot_months", [])) else []

        def fits_season(t):
            return (not t["months"]) or any(mn in rep for mn in t["months"])

        def has_su(t):
            return 1 if t["name"] in SEIKATSU_SUBUNITS else 0

        avail = [t for t in SEIKATSU_THEMES if st["bu"] in t["bu"] and t["name"] not in used]
        season = [t for t in avail if fits_season(t)]   # 季節に合う候補（通年含む）
        pool = season if season else avail               # 無ければ全候補にフォールバック
        if len(pool) <= 1:
            messagebox.showinfo("お知らせ",
                                "この月に合う、入れ替え可能な他のテーマがありません。", parent=win)
            return
        pool.sort(key=has_su, reverse=True)              # 小単元ありを前に
        names = [t["name"] for t in pool]
        ptr = st["theme_ptr"].get(i)
        if ptr is None:
            ptr = names.index(cur_theme["name"]) if cur_theme["name"] in names else -1
        ptr = (ptr + 1) % len(pool)
        st["theme_ptr"][i] = ptr
        th = dict(pool[ptr])
        if th["name"] in SEIKATSU_SUBUNITS:
            th["_subs"] = subunit_default_indices(th["name"], subunit_limit(st["duration"]))
        detail = seikatsu_unit_detail(data, st["bu"], st["stage"], th, st["subjects"])
        st["plan"][i] = (mlabel, th, detail); draw()

    def alt_subunit(i, pos):
        """小単元の別の案：期間で隠れている小単元案に差し替える（時系列は保持）。"""
        mlabel, theme, detail = st["plan"][i]
        full = SEIKATSU_SUBUNITS.get(theme["name"], [])
        cur = list(theme.get("_subs", list(range(len(full)))))
        if pos < 0 or pos >= len(cur):
            return
        shown = set(cur)
        spares = [j for j in range(len(full)) if j not in shown]
        if not spares:
            messagebox.showinfo("お知らせ",
                                "今の期間では、これ以上の小単元案がありません。\n"
                                "期間を「1か月」にすると、別の小単元案に差し替えられます。",
                                parent=win)
            return
        target = cur[pos]
        nxt = min(spares, key=lambda j: (abs(j - target), j))
        cur[pos] = nxt
        cur.sort()                                 # 時系列（導入→本番→まとめ）を保つ
        theme = dict(theme); theme["_subs"] = cur
        st["plan"][i] = (mlabel, theme, detail); draw()

    def out_file(kind):
        if not st["plan"]:
            messagebox.showinfo("確認", "先に単元案を作成してください。", parent=win); return
        labels = data[st["bu"]]["国語科"].get("stage_labels", [])
        skeys = data[st["bu"]]["国語科"].get("stages", [])
        sdisp = labels[skeys.index(st["stage"])] if st["stage"] in skeys else st["stage"]
        fn = f"{st['bu']}_{sdisp}_生活単元学習_年間指導計画"
        try:
            if kind == "word":
                p = filedialog.asksaveasfilename(parent=win, defaultextension=".docx",
                        filetypes=[("Word", "*.docx")], initialfile=fn + ".docx")
                if not p:
                    win.lift(); win.focus_force(); return
                export_seikatsu_word(data, st["bu"], st["stage"], st["subjects"], st["plan"], p, st["duration"])
            else:
                p = filedialog.asksaveasfilename(parent=win, defaultextension=".xlsx",
                        filetypes=[("Excel", "*.xlsx")], initialfile=fn + ".xlsx")
                if not p:
                    win.lift(); win.focus_force(); return
                export_seikatsu_excel(data, st["bu"], st["stage"], st["subjects"], st["plan"], p, st["duration"])
            messagebox.showinfo("完了", "出力しました。", parent=win)
        except Exception as ex:
            messagebox.showerror("エラー", str(ex), parent=win)
        win.lift(); win.focus_force()

    gen()


