def export_word(data, ctx, units, path, duration, show_eval, show_act=True, show_ref=True, show_mat=True):
    from docx import Document
    from docx.shared import Pt, Mm, RGBColor
    from docx.enum.section import WD_ORIENT
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement
    bu, subj, div_idx = ctx
    title = plan_title(data, bu, subj, div_idx, units)
    cols = base_cols(show_act, show_ref, show_mat) + (["評価"] if show_eval else [])
    # A3横 印刷可能幅 約390mm に収まるよう各列幅(mm)を比率配分
    WMM = {"月": 22, "単元名": 28, "段階": 15, "領域": 28, "目標": 60, "学習内容": 80,
           "教材例": 46, "学習活動例": 52, "学習指導要領の関連事項": 60, "評価": 28}
    raw = [WMM[c] for c in cols]
    scale = 390.0 / sum(raw)
    ww_mm = [w * scale for w in raw]
    doc = Document()
    sec = doc.sections[0]; sec.orientation = WD_ORIENT.LANDSCAPE
    sec.page_width = Mm(420); sec.page_height = Mm(297)
    sec.left_margin = Mm(15); sec.right_margin = Mm(15); sec.top_margin = Mm(15); sec.bottom_margin = Mm(15)
    p = doc.add_paragraph()
    run = p.add_run(title); run.bold = True; run.font.size = Pt(16)
    run.font.color.rgb = RGBColor(0x1F, 0x38, 0x64)
    table = doc.add_table(rows=1, cols=len(cols)); table.style = "Table Grid"
    table.autofit = False; table.allow_autofit = False
    layout = OxmlElement('w:tblLayout'); layout.set(qn('w:type'), 'fixed'); table._tbl.tblPr.append(layout)

    def set_w(cell, mm):
        cell.width = Mm(mm)
        tcPr = cell._tc.get_or_add_tcPr()
        tcW = tcPr.find(qn('w:tcW'))
        if tcW is None:
            tcW = OxmlElement('w:tcW'); tcPr.append(tcW)
        tcW.set(qn('w:w'), str(int(mm * 56.7))); tcW.set(qn('w:type'), 'dxa')

    for c, name in enumerate(cols):
        cell = table.rows[0].cells[c]; cell.text = name; set_w(cell, ww_mm[c])
        for pp in cell.paragraphs:
            for r in pp.runs:
                r.font.bold = True; r.font.size = Pt(9)
    for row in _flat_rows(data, bu, subj, div_idx, units, duration, show_eval, show_act, show_ref, show_mat):
        cells = table.add_row().cells
        for c, name in enumerate(cols):
            cells[c].text = row.get(name, ""); set_w(cells[c], ww_mm[c])
            for pp in cells[c].paragraphs:
                for r in pp.runs:
                    r.font.size = Pt(9)
    doc.save(path)


