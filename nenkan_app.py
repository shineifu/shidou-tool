def order_subjects(subjs):
    """教科リストを指導要領順に並べ替える。"""
    return sorted(subjs, key=lambda s: SUBJECT_ORDER.index(s) if s in SUBJECT_ORDER else 99)


def _subj_for_bu(subj, to_bu):
    eq = SUBJ_EQUIV.get(subj, {}).get(to_bu)
    return eq if eq else subj


def stage_step(data, bu, subj, stage, direction):
    """段階を学部横断で上げ下げ。direction=+1/-1。
    戻り値 (新bu,新subj,新stage) または None。"""
    try:
        idx = STAGE_CHAIN.index((bu, stage))
    except ValueError:
        return None
    nidx = idx + direction
    if nidx < 0 or nidx >= len(STAGE_CHAIN):
        return None
    nbu, nstage = STAGE_CHAIN[nidx]
    nsubj = _subj_for_bu(subj, nbu)
    if nsubj not in data.get(nbu, {}):
        return None
    if nstage not in data[nbu][nsubj].get("stages", []):
        return None
    return (nbu, nsubj, nstage)


def map_ryoiki(data, nbu, nsubj, ryoiki):
    """領域名を移動先教科に対応づける。同名→近い領域→None。"""
    div = data[nbu][nsubj]["divisions"][0]
    allr = []
    for grp, ryos in div["content"].items():
        allr += list(ryos.keys())
    if ryoiki in allr:
        return ryoiki
    key = ryoiki.replace("Ａ ", "").replace("Ｂ ", "").replace("Ｃ ", "")
    key = key[:2] if key else ""
    for r in allr:
        if key and key in r:
            return r
    return None


